    <div class="title2">O FIRMIE</div>
    <div class="pasek"></div><br><br>

    <div id="text">
    <p>Początki tej tradycyjnej włoskiej marki sięgają 1974 roku. Podczas gdy najstarszy z trzech braci, 
    Marino, stał się utalentowany i pracowity dwa lata wcześniej w 1972 roku, w 1972 roku został zawodowym
    mistrzem świata i wygrał kilka etapów na wszystkich trzech wyścigach Grand Tour. , najmłodszy Alcido 
    Basso, w tym roku zaczął budować ramy rowerowe w swoim garażu. Cała rodzina miała ciepłe relacje z 
    kolarstwem, a Alcido był wykwalifikowanym mechanikiem w profesjonalnych drużynach swojego brata, 
    a także pasjonatem kolarstwa. Dlatego odniósł sukces i otworzył sklep w 1976 roku i natychmiast po 
    roku formalnie założył pomoc swoim dwóm braciom, markom Basso.</p>
    Firma nadal ma siedzibę na północy Włoch, na obrzeżach Vicenzy, w małej wiosce Dueville. Dzięki 
    dużemu doświadczeniu w produkcji ram stalowych, Alcido współpracowało z producentami rur i 
    inżynierów, aby dopracować swoje ramy wyścigowe w najdrobniejszych szczegółach. W latach 90. 
    firma zaczęła produkować ramy tytanowe, następnie aluminiowe, a następnie wykonane z czarnego 
    złota - węgla.
    <p>Alcido poświęcił się pracy i eksperymentowaniu z tymi materiałami, aby jak najlepiej je zrozumieć. 
    W przypadku motocykli, które z dumą noszą moje imię, mówi z dumą założyciel, używam tylko 
    najlepszych dostępnych materiałów, pochodzących głównie z materiałów przemysłu lotniczego. Są one 
    jednak zaprojektowane i wykonane specjalnie na potrzeby jazdy na rowerze. Moc ciągłego kierowania 
    marką, ciągłego tworzenia nowych rozwiązań technologicznych i innowacji, oparta jest na tradycji 
    włoskiej szkoły jazdy na rowerze szosowym i ramy. Obsesja na punkcie każdego szczegółu i 
    niepowtarzalny włoski design są namacalne.
    Dzięki tym podstawowym zasadom tworzona jest cała kolekcja Basso, która jest w stanie zapewnić 
    wyjątkową wydajność i zmaksymalizować przyjemność z jazdy.</p>
